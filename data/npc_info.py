def get_narrator_from_race_sex(race, sex):
    """Convert race and sex to narrator type"""
    if not race or not sex:
        return "default"
    
    race = race.lower()
    sex = sex.lower()
    
    narrator_map = {
        ('human', 'male'): 'human',
        ('human', 'female'): 'human_female',
        ('dwarf', 'male'): 'dwarf',
        ('dwarf', 'female'): 'dwarf_female',
        ('night_elf', 'male'): 'elf',
        ('night_elf', 'female'): 'elf_female',
        ('gnome', 'male'): 'gnome',
        ('gnome', 'female'): 'gnome_female',
        ('orc', 'male'): 'orc',
        ('orc', 'female'): 'orc_female',
        ('troll', 'male'): 'troll',
        ('troll', 'female'): 'troll_female',
        ('tauren', 'male'): 'tauren',
        ('tauren', 'female'): 'tauren_female',
        ('undead', 'male'): 'undead',
        ('undead', 'female'): 'undead_female',
        ('dragon', 'male'): 'dragon',
        ('dragon', 'female'): 'dragon',
        ('mechanical', 'none'): 'mechanical',
        ('spirit', 'none'): 'spirit',
        ('elemental', 'none'): 'elemental',
        ('wisp', 'none'): 'spirit',
        ('goblin', 'male'): 'gnome',  # Use gnome voice for goblins
        ('goblin', 'female'): 'gnome_female',
    }
    
    return narrator_map.get((race, sex), 'default')

